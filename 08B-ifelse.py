# if 판단문과 if-else 로 판단하는 프로그램

a = 3               # 변수 a 에 3을 저장합니다.

if a ==2:           # a 가 2 와 같은지 비교합니다.
    print ("A")     # False 이므로 이 부분은 실행 되지 않습니다.

if a == 3:          # a 가 3 과 같은지 비교합니다.
    print ("B")     # True 이므로 이 부분이 실행됩니다.

if a == 4:          # a 가 4 와 같은지 비교합니다.
    print ("C")     # False 이므로 이 부분은 실행 되지 않습니다.

else:
    print ("D")     # print("C") 대신 이 부분이 실행됩니다.

    
